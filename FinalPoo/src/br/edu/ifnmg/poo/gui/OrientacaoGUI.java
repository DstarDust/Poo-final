    private void iniciarComboBox1(){
        
          DocenteDAO dao = new DocenteDAO();
    
    ArrayList<Docente> daolist;
    
        try {
            daolist = dao.listarTodos();
            

            for(Docente i : daolist) {
            
            jComboBox1.addItem(i.getNome());
                
            }
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(OrientacaoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    private void iniciarComboBox2(){
        
          DiscenteDAO dao = new DiscenteDAO();
    
    ArrayList<Discente> daolist;
    
        try {
            daolist = dao.listarTodos();
            

            for(Discente i : daolist) {
            
            jComboBox2.addItem(i.getNome());
                
            }
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(OrientacaoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }
    
         

    
    
