    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
              
        
 Orientacao orientacao = new Orientacao();
 orientacao.setDInicio(LocalDate.parse(jTextField4.getText()));
 orientacao.setDFim(LocalDate.parse(jTextField3.getText()));
 orientacao.setRelatorio(jTextField1.getText());
 orientacao.setAprovado(jCheckBox1.isSelected());
  
 
// fazendo a validação dos dados
if ((jTextField1.getText().isEmpty()) || (jTextField3.getText().isEmpty()) || (jTextField4.getText().isEmpty())) { 
    JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
}
else {

    // instanciando a classe UsuarioDAO do pacote dao e criando seu objeto dao
    OrientacaoDAO dao = new OrientacaoDAO();
 

    dao.adiciona(orientacao,jComboBox1.getSelectedItem().toString(),jComboBox2.getSelectedItem().toString());
 
    
    JOptionPane.showMessageDialog(null, "Orientação inserida com sucesso! ");
    
   
}

// apaga os dados preenchidos nos campos de texto
jTextField1.setText("");
jTextField3.setText("");
jTextField4.setText("");


    }//GEN-LAST:event_jButton1ActionPerformed

